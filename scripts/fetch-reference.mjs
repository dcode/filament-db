#!/usr/bin/env node
/**
 * GH #614 — fetch the "FDM Polymers — A Technical Reference" wiki and bundle it
 * into the app as a generated content module.
 *
 * The wiki (github.com/hyiger/filament-db.wiki, branch `master`) is the single
 * source of truth. This script clones it, splits each "Part" page into its
 * numbered chapters, rewrites wiki-relative links to absolute wiki URLs, strips
 * the justification `<div>` wrappers (inline `<sub>/<sup>` are kept and rendered
 * via rehype-raw + rehype-sanitize), and writes `src/content/referenceContent.ts`
 * — a plain TS module so the markdown is compiled into the JS bundle and ships
 * offline inside the Electron asar and the Docker standalone output (no runtime
 * fetch, no file-tracing or process.cwd() gotchas).
 *
 * The generated module is committed (the content is the maintainer's own, CC
 * BY-NC-ND) so the feature renders in dev / CI / web; the release workflows
 * re-run this script before the build to refresh the bundled snapshot.
 *
 *   node scripts/fetch-reference.mjs            # clone + generate
 *   node scripts/fetch-reference.mjs --stub     # write the empty stub (no network)
 */
import { execSync } from "node:child_process";
import { mkdtempSync, readFileSync, writeFileSync, readdirSync, rmSync, mkdirSync } from "node:fs";
import { tmpdir } from "node:os";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const REPO = path.resolve(__dirname, "..");
const CONTENT_DIR = path.join(REPO, "src", "content");
const OUT = path.join(CONTENT_DIR, "referenceContent.ts");
// Tiny companion module: just the chapter-id set + provenance, so the detail
// page can gate the panel WITHOUT pulling the ~300 KB content into its bundle
// (the content is imported only by the lazily-loaded body chunk).
const OUT_INDEX = path.join(CONTENT_DIR, "referenceIndex.ts");
const WIKI = "https://github.com/hyiger/filament-db.wiki.git";
const WIKI_WEB = "https://github.com/hyiger/filament-db/wiki";

const HEADER = `/**
 * AUTO-GENERATED by scripts/fetch-reference.mjs — DO NOT EDIT BY HAND.
 *
 * Source: "FDM Polymers — A Technical Reference" (github.com/hyiger/filament-db wiki).
 * © hyiger · Licensed CC BY-NC-ND 4.0. Bundled verbatim with attribution.
 *
 * This snapshot is committed so the feature renders everywhere. release.yml
 * re-runs the fetch to refresh it before building the desktop installers;
 * Docker / web bundle this committed snapshot as-is.
 */`;

// Only the material chapters (6–21) are reachable from a filament type via
// src/lib/referenceChapter.ts (REFERENCE_CHAPTERS). Foundations (1–5) and the
// cross-cutting workflows (22–28) are never resolved to, so bundling them would
// just bloat the lazy chunk — extract only the material range.
const MATERIAL_MIN = 6;
const MATERIAL_MAX = 21;

/** Write both generated modules: the big content map + the tiny index. */
function emit(sha, syncedAt, content) {
  mkdirSync(CONTENT_DIR, { recursive: true });
  const ids = Object.keys(content).sort((a, b) => Number(a.slice(2)) - Number(b.slice(2)));

  const entries = ids.map((id) => `  ${id}: ${JSON.stringify(content[id])},`).join("\n");
  writeFileSync(
    OUT,
    `${HEADER}

export const REFERENCE_WIKI_SHA = ${JSON.stringify(sha)};
export const REFERENCE_SYNCED_AT = ${JSON.stringify(syncedAt)};

/** Chapter id (e.g. "ch6") -> the chapter's markdown, sliced from the wiki.
 *  Imported ONLY by the lazily-loaded ReferenceChapterBody chunk so it never
 *  lands in the detail page's main bundle. */
export const REFERENCE_CONTENT: Record<string, string> = {
${entries}
};
`,
  );

  writeFileSync(
    OUT_INDEX,
    `${HEADER}

export const REFERENCE_WIKI_SHA = ${JSON.stringify(sha)};
export const REFERENCE_SYNCED_AT = ${JSON.stringify(syncedAt)};

/** Chapter ids present in the bundled content (empty only in the --stub build).
 *  Lets the detail page gate the Technical Reference panel without importing
 *  the heavy content module. */
export const REFERENCE_CHAPTER_IDS: ReadonlySet<string> = new Set([${ids.map((id) => JSON.stringify(id)).join(", ")}]);
`,
  );

  return ids;
}

function writeStub() {
  emit("", "", {});
  console.log(`Wrote empty stub -> ${path.relative(REPO, OUT)} + referenceIndex.ts`);
}

/** Turn one Part page's raw markdown into { chN: markdown }. */
function chaptersFromPage(raw) {
  // 1. Drop the leading breadcrumb line: "[FDM Polymers …](…) ›".
  // 2. Unwrap justification divs (block <div align> wrappers break
  //    markdown-in-HTML and add nothing once rendered).
  // 3. Rewrite wiki-relative links "](FDM-Xxx)" / "](FDM-Xxx#a)" to absolute
  //    wiki URLs (rendered through safeHttpUrl + opened externally in-app).
  const cleaned = raw
    .split("\n")
    .filter((line) => !/^\s*\[[^\]]*\]\([^)]*\)\s*›\s*$/.test(line))
    .filter((line) => !/^\s*<\/?div\b[^>]*>\s*$/i.test(line))
    .join("\n")
    .replace(/\]\((FDM-[A-Za-z0-9-]+(?:#[\w-]+)?)\)/g, (_m, slug) => `](${WIKI_WEB}/${slug})`);

  // Split on numbered chapter headings: "### 6. PLA family".
  const lines = cleaned.split("\n");
  const out = {};
  let curId = null;
  let buf = [];
  const flush = () => {
    if (curId) out[curId] = buf.join("\n").trim() + "\n";
  };
  for (const line of lines) {
    const m = /^###\s+(\d+)\.\s/.exec(line);
    if (m) {
      flush();
      const n = Number(m[1]);
      curId = n >= MATERIAL_MIN && n <= MATERIAL_MAX ? `ch${n}` : null;
      buf = curId ? [line] : [];
    } else if (curId) {
      buf.push(line);
    }
  }
  flush();
  return out;
}

function main() {
  if (process.argv.includes("--stub")) return writeStub();

  const tmp = mkdtempSync(path.join(tmpdir(), "fdb-wiki-"));
  let sha = "";
  try {
    console.log("Cloning wiki (master)…");
    execSync(`git clone --depth 1 ${WIKI} "${tmp}"`, { stdio: "inherit" });
    sha = execSync(`git -C "${tmp}" rev-parse --short HEAD`, { encoding: "utf8" }).trim();

    const content = {};
    for (const file of readdirSync(tmp)) {
      if (!/^FDM-Part-.*\.md$/.test(file)) continue;
      const chapters = chaptersFromPage(readFileSync(path.join(tmp, file), "utf8"));
      Object.assign(content, chapters);
    }

    if (Object.keys(content).length === 0) throw new Error("No chapters extracted — wiki layout changed?");

    // Stamp the wiki commit's date (not wall-clock) so the generated module is a
    // pure function of the source SHA — same wiki commit → byte-identical output,
    // no daily churn, safe for a future drift check.
    const syncedAt = execSync(`git -C "${tmp}" log -1 --format=%cs`, { encoding: "utf8" }).trim();
    const ids = emit(sha, syncedAt, content);
    console.log(`Wrote ${ids.length} chapters (${ids.join(", ")}) @ ${sha} -> ${path.relative(REPO, OUT)} + referenceIndex.ts`);
  } finally {
    rmSync(tmp, { recursive: true, force: true });
  }
}

main();
